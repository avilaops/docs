// Get browser language or default to pt-BR
function getDefaultLanguage() {
    const saved = localStorage.getItem('avx-language');
    if (saved && translations[saved]) return saved;

    const browserLang = navigator.language || navigator.userLanguage;

    // Exact match
    if (translations[browserLang]) return browserLang;

    // Partial match (e.g., 'pt' matches 'pt-BR')
    const partial = Object.keys(translations).find(lang =>
        lang.startsWith(browserLang.split('-')[0])
    );
    if (partial) return partial;

    return 'pt-BR';
}

// Initialize i18n system
function initI18n() {
    const currentLang = getDefaultLanguage();
    document.documentElement.lang = currentLang;

    const selector = document.getElementById('language-selector');
    if (selector) {
        selector.value = currentLang;
        selector.addEventListener('change', (e) => {
            changeLanguage(e.target.value);
        });
    }

    translatePage(currentLang);
}

// Change language
function changeLanguage(lang) {
    if (!translations[lang]) return;

    localStorage.setItem('avx-language', lang);
    document.documentElement.lang = lang;
    translatePage(lang);
}

// Translate all elements with data-i18n attribute
function translatePage(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    const langData = translations[lang];

    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (langData[key]) {
            element.innerHTML = langData[key];
        }
    });
}

// Export for global use
window.i18n = {
    init: initI18n,
    changeLanguage: changeLanguage,
    translations: translations
};

// Auto-initialize when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initI18n);
} else {
    initI18n();
}
