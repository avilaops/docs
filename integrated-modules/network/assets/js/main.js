// Sistema de temas
class ThemeManager {
  constructor() {
    this.currentTheme = localStorage.getItem('theme') || 'light';
    this.applyTheme();
  }

  toggle() {
    this.currentTheme = this.currentTheme === 'light' ? 'dark' : 'light';
    this.applyTheme();
    localStorage.setItem('theme', this.currentTheme);
  }

  applyTheme() {
    document.documentElement.setAttribute('data-theme', this.currentTheme);
    const button = document.getElementById('theme-toggle');
    if (button) {
      button.textContent = this.currentTheme === 'light' ? 'ðŸŒ™' : 'â˜€ï¸';
    }
  }
}

// Sistema de internacionalizaÃ§Ã£o
class I18nManager {
  constructor() {
    this.currentLang = localStorage.getItem('lang') || 'pt-BR';
    this.translations = translations;
  }

  setLanguage(lang) {
    if (this.translations[lang]) {
      this.currentLang = lang;
      localStorage.setItem('lang', lang);
      this.updatePageContent();
    }
  }

  translate(key) {
    return this.translations[this.currentLang][key] || key;
  }

  updatePageContent() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
      const key = element.getAttribute('data-i18n');
      const translation = this.translate(key);

      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translation;
      } else {
        element.textContent = translation;
      }
    });
  }
}

// AnimaÃ§Ãµes e efeitos
class AnimationManager {
  constructor() {
    this.observeElements();
  }

  observeElements() {
    const observer = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('fade-in');
        }
      });
    }, { threshold: 0.1 });

    document.querySelectorAll('.module-card, .section').forEach(el => {
      observer.observe(el);
    });
  }

  animatePerformanceChart(chartId, targetValue) {
    const fill = document.querySelector(`#${chartId} .chart-fill`);
    if (fill) {
      setTimeout(() => {
        fill.style.width = `${targetValue}%`;
      }, 300);
    }
  }

  animateAllCharts() {
    const charts = document.querySelectorAll('.chart-fill');
    charts.forEach((chart, index) => {
      const targetValue = chart.getAttribute('data-value');
      setTimeout(() => {
        chart.style.width = `${targetValue}%`;
      }, index * 200);
    });
  }
}

